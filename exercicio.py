numero_inteiro1 = int(input("Digite o primeiro numero inteiro: "))
numero_inteiro2 = int(input("Digite o segundo numero inteiro: "))
numero_real = float(input("Digite o numero real: "))

produto_do_dobro = (numero_inteiro1 * 2) * (numero_inteiro2 / 2)
print("Resultado do produto do dobro do primeiro com metade do segundo: ", produto_do_dobro)

soma_do_triplo = numero_inteiro1 * 3 + numero_real
print("Resultado da soma do triplo do primeiro com o terceiro: ", soma_do_triplo)

terceiro_elevado_ao_cubo = numero_real ** 3
print("Resultado do terceiro ao cubo: ", terceiro_elevado_ao_cubo)




